#include "3-calc.h"

/**
 * op_add - Adds 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Substracts 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Divides 2 ints and returns the remainder
 *
 * @a: first int
 * @b: second int
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
